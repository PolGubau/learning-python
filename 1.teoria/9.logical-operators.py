# or, and, not

# or
# True or True = True
# True or False = True
# False or True = True
# False or False = False

# and
# True and True = True
# True and False = False
# False and True = False
# False and

# not
# not True = False
# not False = True

# Example
a = 10
b = 20
c = 30

if a > b or a > c:
    print("At least one condition is true")
else:
    print("Both conditions are false")

    